 #operasi aritmatika

a = 8
b = 3
#penjumlahan +
hasil = a + b
print(a,"+", b,"=",hasil)
#pengurangan -
hasil = a - b
print(a,"-",b,"=",hasil)
#perkalian *
hasil = a * b
print(a,"x", b,"=",hasil)
#pembagian /
hasil = a / b
print(a,"/",b,"=",hasil)

#pangkat **
hasil = a ** b
print(a,"pangkat",b,"=",hasil)
#modulus % (sisa pembagian)
hasil = a % b
print(a,"%",b,"=",hasil)
#floar division // (kebalikan dari modulus
hasil = a // b
print(a,"//",b,"=",hasil)
